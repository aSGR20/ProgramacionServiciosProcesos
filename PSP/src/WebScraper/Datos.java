package WebScraper;

public class Datos {
	String nombre;
	String anterior;
	String ultimo;
	String difPorcentaje;
	String maximo;
	String minimo;
	String fecha;
	String hora;
	String difPorcentaje2021;
	
	public Datos() {
	}
	
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getAnterior() {
		return anterior;
	}
	public void setAnterior(String anterior) {
		this.anterior = anterior;
	}
	public String getUltimo() {
		return ultimo;
	}
	public void setUltimo(String ultimo) {
		this.ultimo = ultimo;
	}
	public String getDifPorcentaje() {
		return difPorcentaje;
	}
	public void setDifPorcentaje(String difPorcentaje) {
		this.difPorcentaje = difPorcentaje;
	}
	public String getMaximo() {
		return maximo;
	}
	public void setMaximo(String maximo) {
		this.maximo = maximo;
	}
	public String getMinimo() {
		return minimo;
	}
	public void setMinimo(String minimo) {
		this.minimo = minimo;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public String getHora() {
		return hora;
	}
	public void setHora(String hora) {
		this.hora = hora;
	}
	public String getDifPorcentaje2021() {
		return difPorcentaje2021;
	}
	public void setDifPorcentaje2021(String difPorcentaje2021) {
		this.difPorcentaje2021 = difPorcentaje2021;
	}
}
